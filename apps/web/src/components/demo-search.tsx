"use client";

import { useMemo, useState, type FormEvent } from "react";
import { RotateCcw, Search, ShieldCheck } from "lucide-react";
import { Alert } from "@/components/ui/alert";
import { Badge } from "@/components/ui/badge";
import { Button } from "@/components/ui/button";
import { Card } from "@/components/ui/card";
import { Input } from "@/components/ui/input";
import { Skeleton } from "@/components/ui/skeleton";
import {
  DEMO_DISCLAIMER,
  type SearchResponse,
  type SearchResult,
} from "@/lib/search/types";

const examples = [
  "altered mitochondrial organization",
  "lysosomal stress phenotype",
  "disrupted cytoskeletal organization",
  "reduced nuclear texture",
  "increased cellular granularity",
];

export function DemoSearch() {
  const [query, setQuery] = useState(examples[0]);
  const [response, setResponse] = useState<SearchResponse | null>(null);
  const [error, setError] = useState("");
  const [loading, setLoading] = useState(false);
  const canSubmit = useMemo(() => query.trim().length > 0 && query.length <= 240, [query]);

  async function submit(event?: FormEvent<HTMLFormElement>) {
    event?.preventDefault();
    setError("");
    setResponse(null);
    setLoading(true);
    try {
      const result = await fetch("/api/search", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ query }),
      });
      const payload = await result.json();
      if (!result.ok) {
        setError(payload.error ?? "Search failed.");
        return;
      }
      setResponse(payload);
    } catch {
      setError("The illustrative search service is unavailable.");
    } finally {
      setLoading(false);
    }
  }

  function reset() {
    setQuery("");
    setResponse(null);
    setError("");
  }

  return (
    <div className="grid gap-6 lg:grid-cols-[0.9fr_1.1fr]">
      <Card>
        <Badge className="border-violet/20 bg-violet/10 text-violet">{DEMO_DISCLAIMER}</Badge>
        <h1 className="mt-5 text-4xl font-semibold leading-tight">
          Explore the retrieval interface before the real model is connected
        </h1>
        <p className="mt-4 text-ink/70">
          This page demonstrates interaction design only. Results are synthetic,
          anonymized, and generated by a mock provider.
        </p>
        <form className="mt-8 space-y-4" onSubmit={submit}>
          <label className="text-sm font-semibold text-ink" htmlFor="demo-query">
            Biological phenotype query
          </label>
          <Input
            id="demo-query"
            value={query}
            onChange={(event) => setQuery(event.target.value)}
            placeholder="e.g. altered mitochondrial organization"
            aria-describedby="query-help"
          />
          <p id="query-help" className="text-xs text-ink/55">
            Queries are not stored. Keep this demo to broad biological concepts.
          </p>
          <div className="flex flex-wrap gap-2">
            {examples.map((example) => (
              <button
                key={example}
                type="button"
                className="rounded-full border border-ink/10 bg-white/70 px-3 py-1.5 text-xs font-semibold text-ink/70 transition hover:bg-white focus-visible:outline focus-visible:outline-2 focus-visible:outline-offset-2 focus-visible:outline-teal"
                onClick={() => setQuery(example)}
              >
                {example}
              </button>
            ))}
          </div>
          <div className="flex flex-wrap gap-3">
            <Button disabled={!canSubmit || loading} type="submit">
              <Search className="h-4 w-4" /> Submit query
            </Button>
            <Button variant="secondary" type="button" onClick={reset}>
              <RotateCcw className="h-4 w-4" /> Reset
            </Button>
          </div>
        </form>
      </Card>

      <div aria-live="polite" className="space-y-4">
        <Alert>
          <div className="flex gap-3">
            <ShieldCheck className="mt-0.5 h-5 w-5 text-teal" />
            <div>
              <div className="font-semibold">Leakage status</div>
              <p className="mt-1 text-ink/65">
                No treatment identifiers, target sequences, plate labels, or well
                labels are used in this illustrative demo.
              </p>
            </div>
          </div>
        </Alert>
        {loading ? <LoadingResults /> : null}
        {error ? <Alert className="border-red-200 bg-red-50 text-red-900">{error}</Alert> : null}
        {!loading && !error && !response ? (
          <Card className="text-sm text-ink/65">
            Submit a broad phenotype query to see synthetic ranked results.
          </Card>
        ) : null}
        {response ? (
          <>
            <p className="text-sm font-semibold text-ink">{response.disclaimer}</p>
            {response.results.map((result, index) => (
              <ResultCard key={result.id} result={result} rank={index + 1} />
            ))}
          </>
        ) : null}
      </div>
    </div>
  );
}

function LoadingResults() {
  return (
    <div className="space-y-3">
      <Skeleton className="h-28" />
      <Skeleton className="h-28" />
      <Skeleton className="h-28" />
    </div>
  );
}

function ResultCard({ result, rank }: { result: SearchResult; rank: number }) {
  return (
    <Card className="shadow-none">
      <div className="flex items-start justify-between gap-4">
        <div>
          <div className="text-sm font-semibold text-teal">Rank {rank}</div>
          <h2 className="mt-1 text-xl font-semibold">{result.label}</h2>
          <p className="mt-2 text-sm leading-6 text-ink/65">{result.description}</p>
        </div>
        <div
          className="rounded-md bg-ink px-3 py-2 text-sm font-semibold text-white"
          title="Illustrative score from mock synthetic tags, not a real model confidence."
        >
          {result.score.toFixed(3)}
        </div>
      </div>
      <div className="mt-4 flex flex-wrap gap-2">
        <Badge className="bg-teal/10 text-teal">synthetic result</Badge>
        {result.evidenceTags.map((tag) => (
          <Badge key={tag}>{tag}</Badge>
        ))}
      </div>
      <p className="mt-4 text-sm leading-6 text-ink/70">{result.morphologySummary}</p>
      <p className="mt-2 text-xs text-ink/50">{result.explanation}</p>
    </Card>
  );
}
